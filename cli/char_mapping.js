// Lazy initialization to avoid circular dependency
let _gridCharMap = null

export function getGridCharMap() {
  if (!_gridCharMap) {
    _gridCharMap = Object.keys(charMap).reduce((acc, key) => {
      acc[key] = new Grid({ content: charMap[key].join('\n') })
      return acc
    }, {})
  }
  return _gridCharMap
}

